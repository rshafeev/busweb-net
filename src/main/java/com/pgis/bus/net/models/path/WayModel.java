package com.pgis.bus.net.models.path;

public class WayModel {

	protected double distance;
	protected int moveTimeSecs;
	
	public double getDistance() {
		return distance;
	}
	public void setDistance(double distance) {
		this.distance = distance;
	}
	public int getMoveTimeSecs() {
		return moveTimeSecs;
	}
	public void setMoveTimeSecs(int moveTimeSecs) {
		this.moveTimeSecs = moveTimeSecs;
	}
	
}
